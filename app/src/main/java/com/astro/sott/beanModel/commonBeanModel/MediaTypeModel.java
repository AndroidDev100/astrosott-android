package com.astro.sott.beanModel.commonBeanModel;

public class MediaTypeModel {
    private String name;
    private String id;

    public MediaTypeModel(String name, String id) {
        this.name = name;
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }
}
